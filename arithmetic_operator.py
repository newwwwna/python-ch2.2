# 사칙연산

print(2+3)
print(2-3)
print(2*3)
print(2/3)
print(2/3.0)
print(2.0/3)
print(2.0/3.0)

# //(몫), **(지수승), %(나머지)
print(2//3)
print(2**3)
print(2%3)

# divmod(a,b) = 몫, 나머지
print(divmod(2,3))

# 연산자 우선 순위
print(2+3*4)
print(-(2+3)*4)

# 결합 우선 순위
#뒤에부터 먼저함~~
print(2**3**4)
print((2**3)**4)
print(2**(3**4))

